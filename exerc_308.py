"""
Write a code who read a value in meters, convert and show in millimeters.
"""
value_meters = int(input('We are going to convert meters in millimeters. Enter, a with number: '))
print('You type ', value_meters, 'm')
converting_millimeters = (value_meters * 1000)
print('The conversion has been finishing. You have', converting_millimeters, 'millimeters')
